def fix_driver_terms(text: str) -> str:
    """водитель (chauffeur MT) -> драйвер, without breaking руководитель/производитель."""
    protected = (
        ("__PROIZVODITELI__", "производителей"),
        ("__PROIZVODITELNOST__", "производительности"),
        ("__PROIZVODITELNOST2__", "Производительность"),
        ("__PROIZVODSTVENNO__", "производственно"),
        ("__PROIZVODSTVENNYH__", "ПРОИЗВОДСТВЕННЫХ"),
        ("__RUKOVODITEL__", "руководитель"),
        ("__RUKOVODITELYA__", "руководителя"),
        ("__RUKOVODITELU__", "руководителю"),
        ("__RUKOVODITELI__", "Руководитель"),
    )
    for token, word in protected:
        text = text.replace(word, token)
    for old, new in [
        ("водителей", "драйверов"),
        ("водителям", "драйверам"),
        ("водителя", "драйвера"),
        ("водители", "драйверы"),
        ("Водитель", "Драйвер"),
        ("водитель", "драйвер"),
    ]:
        text = text.replace(old, new)
    for token, word in protected:
        text = text.replace(token, word)
    return text


def fix_header_spacing(text: str) -> str:
    """##Заголовок -> ## Заголовок (missing space after #)."""
    return re.sub(r"^(#{1,6})([^\s#])", r"\1 \2", text, flags=re.MULTILINE)


def main() -> int:
    count = 0
    for path in sorted(RU.rglob("*.md")):
        if path.name.lower() in SKIP:
            continue
        text = path.read_text(encoding="utf-8")
        orig = text
        for old, new in REPLACEMENTS:
            text = text.replace(old, new)
        text = fix_driver_terms(text)
        text = fix_header_spacing(text)
        if text != orig:
            path.write_text(text, encoding="utf-8")
            count += 1
            print(path.relative_to(ROOT))
    print(f"Updated {count} files")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
